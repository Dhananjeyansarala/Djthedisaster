package Preparartion2;

public class Book {
	String name;
	int price;
	boolean isQuality;
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
	public void setIsQuality(boolean isQuality) {
		this.isQuality=isQuality;
	}
	public boolean getIsQuality() {
		return isQuality;
	}

}
